package LukasPackage;

public class Taschencalc1 {

	private float x, y;

	public void setx(float a) {

		this.x = a;
		
	}

	public float getx() {

		return this.x;
	}

	public void sety(float a) {

		this.y = a;
		
	}

	public float gety() {

		return this.y;
	}
	public float addi() {

		return x + y;
	}

	public float multi() {

		return x * y;

	}

	public float divi() {

		return x / y;
	}
	
	public float subtrack() {
		
		return x - y;
	}

	




}
